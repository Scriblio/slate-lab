"""One reproducible command -> one results table.

Runs the no-API experiments and tabulates their JSON into RESULTS.md + stdout,
so a reader can regenerate the headline numbers with a single command.

  python make_results.py           # full  (30 / 5 seeds; a few minutes)
  python make_results.py --fast    # quick (5 / 2 seeds)

The LLM legs (distill_llm.py, transplant.py) are intentionally NOT run here -
they need an ANTHROPIC_API_KEY and cost money.
"""
import argparse
import json
import os
import subprocess
import sys

HERE = os.path.dirname(os.path.abspath(__file__))


def _run(script, *args):
    print(f"  running {script} {' '.join(args)} ...", flush=True)
    subprocess.run([sys.executable, script, *args], cwd=HERE, check=True,
                   stdout=subprocess.DEVNULL, stderr=subprocess.STDOUT)


def _load(name):
    with open(os.path.join(HERE, name)) as f:
        return json.load(f)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--fast", action="store_true")
    a = ap.parse_args()
    sb, sf = ("5", "2") if a.fast else ("30", "5")
    print("Running no-API experiments (this takes a few minutes)...")
    _run("bench_vs_baselines.py", "--seeds", sb)
    _run("bench_program_family.py", "--seeds", sf)
    vb, pf = _load("results_vs_baselines.json"), _load("results_program_family.json")

    hi = str(max(vb["sigmas"]))
    out = ["# Reproduced results",
           f"\n_Generated by `python make_results.py"
           f"{' --fast' if a.fast else ''}`._\n",
           "## Store comparison - div-7 automaton (`bench_vs_baselines.py`)",
           f"\nThe *same* transition table executed three ways, "
           f"{vb['config']['seeds']} seeds; end-to-end accuracy under noisy "
           f"state-reads:\n",
           f"| store | clean | noisy (sigma={hi}) | bytes/rule (lab) |",
           "|---|---|---|---|"]
    for k in ("dict", "knn", "slate"):
        out.append(f"| {k} | {vb['task_acc'][k]['0.0'][0]:.0%} | "
                   f"{vb['task_acc'][k][hi][0]:.0%} | {vb['bytes_per_rule'][k]} |")

    A, cap = pf["aggregate"], pf["capacity_one_slate"]
    out += [f"\n## Shared DFA interpreter -{pf['n_programs']} finite-state "
            f"programs (`bench_program_family.py`)",
            f"\nOne task-agnostic interpreter, {pf['config']['seeds']} seeds:\n",
            f"| store | clean | noisy (sigma={pf['config']['sigma']}) |",
            "|---|---|---|",
            f"| dict | {A['dict']['clean']:.0%} | {A['dict']['noisy']:.0%} |",
            f"| slate | {A['slate']['clean']:.0%} | {A['slate']['noisy']:.0%} |",
            f"\nCapacity: all {cap['total_rules']} rules of all "
            f"{pf['n_programs']} programs in one Slate -> "
            f"{cap['clean_acc_all_in_one']:.0%} clean (no cross-talk).\n"]

    text = "\n".join(out) + "\n"
    with open(os.path.join(HERE, "RESULTS.md"), "w") as f:
        f.write(text)
    print("\n" + text + "-> RESULTS.md")


if __name__ == "__main__":
    main()
